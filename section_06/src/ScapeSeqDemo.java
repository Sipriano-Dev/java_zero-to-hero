public class ScapeSeqDemo {

    public static void main(String[] args) {
        String name = "\"Anderson\"";//Scape sequence character
        System.out.println(name);

        String m = "\u004D"; //M
        System.out.println(m);
        String unicodeName = "\u004Dadan\u0021";//Can mix unicode
        System.out.println(unicodeName);

    }

}
